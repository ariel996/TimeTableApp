    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String name = nameTextfield.getText();
        String surname = surnameTextfield.getText();
        String telephone = telephoneTextfield.getText();
        String email = emailTextfield.getText();
        try {
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/mydb", "root", "arielnana020596");
            PreparedStatement insert = connection.prepareStatement("INSERT INTO teachers(name, surname, telephone, email) VALUES (?,?,?,?)");
            insert.setString(1, name);
            insert.setString(2, surname);
            insert.setString(3, telephone);
            insert.setString(4, email);
            insert.executeUpdate();
            JOptionPane.showMessageDialog(this, "Teacher added");
            
            // reinitialisation of textfields
            nameTextfield.setText("");
            surnameTextfield.setText("");
            telephoneTextfield.setText("");
            emailTextfield.setText("");
            nameTextfield.requestFocus();
            table_update();
        } catch(SQLException exception) {
            exception.printStackTrace();
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // update function
        DefaultTableModel model = (DefaultTableModel) teacherTable.getModel();
        int selectedIndex = teacherTable.getSelectedRow();
        try {
            int Id = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
            String name = nameTextfield.getText();
            String surname = surnameTextfield.getText();
            String telephone = telephoneTextfield.getText();
            String email = emailTextfield.getText();
            
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/mydb", "root", "arielnana020596");
            PreparedStatement insert = connection.prepareStatement("UPDATE teachers SET name=?, surname=?, telephone=?, email=? WHERE id=?");
            insert.setString(1, name);
            insert.setString(2, surname);
            insert.setString(3, telephone);
            insert.setString(4, email);
            insert.setInt(4, Id);
        } catch(SQLException exception) {
            exception.printStackTrace();
        }
    }//GEN-LAST:event_updateBtnActionPerformed

