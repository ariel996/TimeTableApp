    private void table_update() {
        int CC;
        try {
            Connection connection = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb", "root", "arielnana020596");
            connection = DriverManager.getConnection("jdbc:mysql://localhost/mydb", "root", "arielnana020596");
            PreparedStatement st = connection.prepareStatement("SELECT * FROM teachers");
            ResultSet rs = st.executeQuery();
            ResultSetMetaData RSMD = rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) teacherTable.getModel();
            DFT.setRowCount(0);
            
            while(rs.next()) {
                Vector v2 = new Vector();
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(rs.getString("name"));
                    v2.add(rs.getString("surname"));
                    v2.add(rs.getString("telephone"));
                    v2.add(rs.getString("email"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

